  padding-bottom: 0;
  padding-left: 20%;">
  Utilizando o mesmo teclado citado no primeiro passo, digite o seu peso em quilogramas (kg) no campo abaixo da frase "Digita o seu peso",
  assim como mostra a imagem acima. Após isso, pressione a tecla Enter ou Tab do seu teclado para mover o cursor para o botão Calcular.<br><br>
  </p>
  <center>
  <h4> Terceiro passo: </h4>
  </center>
  <br>
  <img src="img/bmi3.png" width="50%" height="50%">
