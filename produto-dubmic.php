  padding-right: 20%;
  padding-bottom: 0;
  padding-left: 20%;">
  Utilizando o mesmo teclado citado no primeiro e no segundo passo ou um mouse (de preferência funcionando), pressione a tecla ENTER ou clique no botão calcular (Você também pode utilizar o atalho Alt + C).
  Se você inseriu seu peso e altura da forma certa, uma mensagem será exibida mostrando o seu IMC e a sua condição atual (abaixo do peso, acima do peso, ou no peso ideal).
  Agora aperte no botão "OK" para fechar a mensagem.<br><br>
  </p>
